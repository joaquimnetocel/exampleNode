export const functionSum = function (argFirstNumber, argSecondNumber) {
	return argFirstNumber + argSecondNumber;
};

export const functionSubtraction = function (argFirstNumber, argSecondNumber) {
	return argFirstNumber - argSecondNumber;
};

export const functionProduct = function (argFirstNumber, argSecondNumber) {
	return argFirstNumber * argSecondNumber;
};

export const functionDivision = function (argFirstNumber, argSecondNumber) {
	return argFirstNumber * argSecondNumber;
};
